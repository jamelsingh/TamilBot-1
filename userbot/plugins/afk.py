@borg.on(admin_cmd(pattern=r"afk ?(.*)", outgoing=True))
async def _(event):
    if event.fwd_from:
        return
    global USERAFK_ON
    global afk_time
    global last_afk_message
    global afk_start
    global afk_end
    global reason
    USERAFK_ON = {}
    afk_time = None
    last_afk_message = {}
    afk_end = {}
    if not USERAFK_ON:
        start_1 = datetime.now()
        afk_start = start_1.replace(microsecond=0)
        reason = event.pattern_match.group(1)
        last_seen_status = await borg(
            functions.account.GetPrivacyRequest(types.InputPrivacyKeyStatusTimestamp())
        )
        if isinstance(last_seen_status.rules, types.PrivacyValueAllowAll):
            afk_time = datetime.now()
        USERAFK_ON = f"on: {reason}"
        if reason:
            await borg.send_message(
                event.chat_id, f"**роиро╛ройрпН Offline роЪрпЖро▓рпНроХро┐ро▒рпЗройрпН!** __роПройрпЖройро┐ро▓рпН,  {reason}__"
            )
        else:
            await borg.send_message(event.chat_id, '**роиро╛ройрпН Offline роЪрпЖро▓рпНроХро┐ро▒рпЗройрпН!**')
        await asyncio.sleep(5)
        await event.delete()
        if BOTLOG:
            await borg.send_message(
                BOTLOG_CHATID,
                f"#AFKTRUE \nSet AFK mode to True, and Reason is {reason}",
            )
